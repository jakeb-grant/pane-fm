export function getIconForEntry(entry: {
	is_dir: boolean;
	is_symlink: boolean;
	mime_type: string;
	name: string;
}): string {
	if (entry.is_symlink) return "\uF0C1";
	if (entry.is_dir) return "\uF07B";

	// Check exact filename (case-insensitive)
	const nameLower = entry.name.toLowerCase();
	const nameIcon = NAME_ICONS[nameLower];
	if (nameIcon) return nameIcon;

	// Check compound extensions (e.g. spec.ts, d.ts)
	const parts = entry.name.split(".");
	if (parts.length > 2) {
		const compoundExt = parts.slice(-2).join(".").toLowerCase();
		const compoundIcon = EXT_ICONS[compoundExt];
		if (compoundIcon) return compoundIcon;
	}

	// Check simple extension
	const ext = parts.pop()?.toLowerCase() ?? "";
	const extIcon = EXT_ICONS[ext];
	if (extIcon) return extIcon;

	// MIME type fallback
	const mime = entry.mime_type;
	if (mime.startsWith("image/")) return "\uE60D";
	if (mime.startsWith("video/")) return "\uE69F";
	if (mime.startsWith("audio/")) return "\uF001";
	if (mime.startsWith("text/")) return "\uF15C";

	return "\uF15B";
}
